    private void bSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSimpanActionPerformed
        // TODO add your handling code here:
        String Text = lHasil.getText();
        File log = new File("Hasil.txt");
        try {
            if (log.exists() == false){
                JOptionPane.showMessageDialog(null, "Berhasil membuat file Hasil.txt");
                log.createNewFile();
            }
            
            PrintWriter out = new PrintWriter(new FileWriter(log, true));
            out.append(Text).println();
            out.close();
        } catch (IOException e){
            System.out.print("Gagal memuat log");
        }
        JOptionPane.showMessageDialog(null, "Berhasil menyimpan text : " + Text + " | di file Hasil.txt");
    }//GEN-LAST:event_bSimpanActionPerformed

