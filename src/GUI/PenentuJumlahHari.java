    private void filterKeyTyped(java.awt.event.KeyEvent evt){
        char c = evt.getKeyChar();
        if (! (Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE)) || (c == KeyEvent.VK_DELETE)){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Masukkan hanya 0-9");
            evt.consume();
        }
    }
    
    private void bHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHitungActionPerformed
        // TODO add your handling code here:
        if(tTahun.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Error : isian tahun kosong!", "Error : isian tahun kosong!", JOptionPane.WARNING_MESSAGE);
        } else {
            int tahun = Integer.parseInt(tTahun.getText());
            int jumlahHari;
            if(cBulan.getSelectedItem().equals("Februari")){
                if ( (tahun % 4 == 0) && !(tahun % 100 == 0) || (tahun % 400 == 0))
                    jumlahHari   = 29;
                else jumlahHari = 28;
            } else if(cBulan.getSelectedItem().equals("April") ||
                    cBulan.getSelectedItem().equals("Juni") ||
                    cBulan.getSelectedItem().equals("September") ||
                    cBulan.getSelectedItem().equals("November")){
                jumlahHari = 31;
            } else {
                jumlahHari = 30;
            }
            lHasil.setText("Jumlah hari pada bulan " + cBulan.getSelectedItem() + " tahun " + tahun + " adalah " + jumlahHari);
        }
    }//GEN-LAST:event_bHitungActionPerformed

